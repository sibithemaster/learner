package com.days.datatypes;

public class Demo {

	public static void main(String[] args) {
       int num=200,number=100;
       //sop
       System.out.println(num+number); //300
       System.out.println(num-number); //100
       System.out.println(num*number); //20000
       System.out.println(num/number); //2
       System.out.println(num%number); //0
  
	}

}
