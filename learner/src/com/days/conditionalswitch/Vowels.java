package com.days.conditionalswitch;

public class Vowels {

	public static void main(String[] args) {
        char character='U';
        
        switch(character) {
		case 'a':System.out.println(character+" is vowel");break;
		case 'e':System.out.println(character+" is vowel");break;
		case 'i':System.out.println(character+" is vowel");break;
		case 'o':System.out.println(character+" is vowel");break;
		case 'u':System.out.println(character+" is vowel");break;
		case 'A':System.out.println(character+" is vowel");break;
		case 'E':System.out.println(character+" is vowel");break;
		case 'I':System.out.println(character+" is vowel");break;
		case 'O':System.out.println(character+" is vowel");break;
		case 'U':System.out.println(character+" is vowel");break;
		default:System.out.println(character+" is not a vowel");break;

		}
	}

}
